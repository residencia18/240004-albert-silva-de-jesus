create table tb_concorrente (
    id bigint generated by default as identity,
    cpf varchar(255),
    nome varchar(255),
    primary key (id)
);

create table tb_lance (
    valor float(53),
    concorrente_id bigint,
    id bigint generated by default as identity,
    leilao_id bigint,
